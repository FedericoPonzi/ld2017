              <p class="lead">Un <i>Linux User Group (LUG)</i>, o anche “Linux Users Group” e “Linux Users’ Group”, è un gruppo formato da persone che osservano i principi del software libero ed impegnate nel diffondere i sistemi operativi basati sul kernel Linux.</p>
              <h5 class="title">Ma cos'è effettivamente un LUG?</h5>
              <p>Spesso un LUG è una associazione non-profit, formata da persone liberamente costituite, che svolgono la loro attività prevalentemente su base volontaria e che provvedono al supporto e alla diffusione di GNU/Linux, in particolar modo presso chi si avvicina per la prima volta a tale sistema operativo. Può materializzarsi in un contesto fisico, ponendosi come punto di riferimento a livello locale per i temi toccati, oppure virtualmente in forma di community online.</p>
